// 5. Dùng vòng lặp while, in ra tất cả số nguyên dương lẻ < 100
var x = 1;
while (x < 100) {
    console.log(x);
    x += 2; // x = x + 2
}

// 6. Dùng vòng lặp while, in ra tất cả số nguyên dương chẵn < 100
var i = 2;
while (i < 100) {
    console.log(i);
    i += 2; // i = i + 2
}

// 7. Dùng vòng lặp while, in ra tất cả số nguyên âm lẻ > -100
var j = -1;
while (j > -100) {
    console.log(j);
    j -= 2; // j = j - 2
}

